  function drag(e) {
    if (!isDragging) return;

    const deltaX = e.clientX - dragStartX;
    const deltaY = e.clientY - dragStartY;

    const newX = panelStartX + deltaX;
    const newY = panelStartY + deltaY;

    const maxX = window.innerWidth - reviewPanel.offsetWidth;
    const maxY = window.innerHeight - reviewPanel.offsetHeight;

    reviewPanel.style.left = Math.max(0, Math.min(newX, maxX)) + 'px';
    reviewPanel.style.top = Math.max(0, Math.min(newY, maxY)) + 'px';
    reviewPanel.style.right = 'auto';
    reviewPanel.style.bottom = 'auto';
  }

