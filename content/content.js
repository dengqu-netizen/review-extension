  function updateMarkerPosition(marker, element) {
    if (!element || !document.body.contains(element)) {
      marker.style.display = 'none';
      return;
    }

    const rect = element.getBoundingClientRect();

    // 判断元素是否在视口外（被滚动隐藏）
    // 如果元素完全不可见，隐藏角标
    if (rect.bottom < 0 || rect.top > window.innerHeight ||
        rect.right < 0 || rect.left > window.innerWidth) {
      marker.style.display = 'none';
      return;
    }

    marker.style.position = 'fixed';
    marker.style.top = (rect.top - 10) + 'px';
    marker.style.left = (rect.right - 10) + 'px';
    marker.style.display = 'flex';
  }

  // 更新所有可见角标位置
  function updateAllVisibleMarkerPositions() {
    if (!isPanelVisible) return; // 面板隐藏时不需要更新

    annotationMarkerMap.forEach(({ marker, element }, annotationId) => {
      // 如果运行时引用丢失（页面刷新后），尝试重新查找
      if (!element || !document.body.contains(element)) {
        const annotation = annotations.find(a => a.id === annotationId);
        if (annotation) {
          const found = findElementSafe(annotation.selector);
          if (found) {
            annotationMarkerMap.set(annotationId, { marker, element: found });
            annotationElementMap.set(annotationId, found);
            element = found;
          }
        }
      }
      updateMarkerPosition(marker, element);
    });
  }

  // 重建角标并显示（面板打开时调用）
  function rebuildAndShowMarkers() {
    annotations.forEach(annotation => {
      if (annotationMarkerMap.has(annotation.id)) {
        // 角标已存在，显示并更新位置
        const { marker, element } = annotationMarkerMap.get(annotation.id);
        // 检查元素是否还在 DOM 中
        if (element && document.body.contains(element)) {
          updateMarkerPosition(marker, element);
        } else {
          // 运行时引用丢失，尝试通过 selector 找回
          const found = findElementSafe(annotation.selector);
          if (found) {
            annotationMarkerMap.set(annotation.id, { marker, element: found });
            annotationElementMap.set(annotation.id, found);
            updateMarkerPosition(marker, found);
          } else {
            marker.style.display = 'none';
          }
        }
      } else {
        // 角标不存在（首次加载或页面刷新后），创建
        const element = findElementSafe(annotation.selector);
        if (element) {
          annotationElementMap.set(annotation.id, element);
          createMarker(annotation, element);
        }
      }
    });
  }

