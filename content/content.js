  // ==================== 双向联动核心 ====================

  function locateAnnotation(annotationId) {
    const annotation = annotations.find(a => a.id === annotationId);
    if (!annotation) return;

    // 优先从运行时映射获取，其次通过 selector 查找
    let element = annotationElementMap.get(annotationId);
    if (!element || !document.body.contains(element)) {
      element = findElementSafe(annotation.selector);
      if (element) {
        annotationElementMap.set(annotationId, element);
      }
    }

    if (element) {
      element.scrollIntoView({ behavior: 'smooth', block: 'center' });

      selectedElement = element;

      // 延迟更新位置，等 scrollIntoView 动画完成
      setTimeout(() => {
        updateSelectOverlay(element);
      }, 350);
    }

    // 高亮对应的评论卡片
    highlightCommentCard(annotationId);
  }

  function highlightCommentCard(annotationId) {
    const items = document.querySelectorAll('.dq-review-comment-item');
    items.forEach(item => {
      if (parseInt(item.dataset.annotationId) === annotationId) {
        item.classList.add('selected');
        item.scrollIntoView({ behavior: 'smooth', block: 'center' });
      } else {
        item.classList.remove('selected');
      }
    });
  }

  // ==================== 删除标注 ====================
  function deleteAnnotation(annotationId) {
    annotations = annotations.filter(a => a.id !== annotationId);
    saveAnnotations();

    // 销毁角标 DOM（删除是真删除）
    const entry = annotationMarkerMap.get(annotationId);
    if (entry && entry.marker) {
      entry.marker.remove();
    }
    annotationMarkerMap.delete(annotationId);
    annotationElementMap.delete(annotationId);

    // 如果当前选中的是被删除的标注，隐藏覆盖层
    hideSelectOverlay();

    updateCommentList();
    updateCommentBadge();
  }

  // ==================== 编辑标注 ====================
  function editAnnotation(annotationId) {
    const annotation = annotations.find(a => a.id === annotationId);
    if (!annotation) return;

    const itemEl = document.querySelector(`.dq-review-comment-item[data-annotation-id="${annotationId}"]`);
    if (!itemEl) return;

    const contentEl = itemEl.querySelector('.dq-review-comment-item-content');
    if (!contentEl) return;

    if (contentEl.querySelector('textarea')) return;

    const originalContent = annotation.content;

    const textarea = document.createElement('textarea');
    textarea.className = 'dq-review-edit-textarea';
    textarea.value = originalContent;
    textarea.style.cssText = `
      width: 100%;
      min-height: 80px;
      padding: 8px;
      border: 1px solid #667eea;
      border-radius: 6px;
      font-size: 14px;
      font-family: inherit;
      resize: vertical;
      box-sizing: border-box;
    `;

    const btnContainer = document.createElement('div');
    btnContainer.style.cssText = `
      display: flex;
      gap: 8px;
      margin-top: 8px;
      justify-content: flex-end;
    `;

    const saveBtn = document.createElement('button');
    saveBtn.textContent = t('save');
    saveBtn.className = 'dq-review-btn dq-review-btn-primary';
    saveBtn.style.cssText = `
      padding: 6px 12px;
      border: none;
      border-radius: 4px;
      font-size: 13px;
      font-weight: 500;
      cursor: pointer;
      background: #667eea;
      color: white;
    `;
    saveBtn.onclick = (e) => {
      e.stopPropagation();
      const newContent = textarea.value.trim();
      if (newContent && newContent !== originalContent) {
        annotation.content = newContent;
        saveAnnotations();
      }
      updateCommentList();
    };

    const cancelBtn = document.createElement('button');
    cancelBtn.textContent = t('cancel');
    cancelBtn.className = 'dq-review-btn dq-review-btn-secondary';
    cancelBtn.style.cssText = `
      padding: 6px 12px;
      border: none;
      border-radius: 4px;
      font-size: 13px;
      font-weight: 500;
      cursor: pointer;
      background: #e5e7eb;
      color: #374151;
    `;
    cancelBtn.onclick = (e) => {
      e.stopPropagation();
      updateCommentList();
    };

    btnContainer.appendChild(saveBtn);
    btnContainer.appendChild(cancelBtn);

    contentEl.innerHTML = '';
    contentEl.appendChild(textarea);
    contentEl.appendChild(btnContainer);

    textarea.focus();
    textarea.select();
  }

