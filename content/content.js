  // ==================== 下载 Markdown 文件 ====================
  function downloadMarkdown(markdown) {
    const today = new Date();
    const year = today.getFullYear();
    const month = String(today.getMonth() + 1).padStart(2, '0');
    const day = String(today.getDate()).padStart(2, '0');
    const dateForFilename = `${year}-${month}-${day}`;
    const pageName = window.location.pathname.split('/').pop().replace('.html', '') || 'prototype';
    const filename = `${pageName}-prototype-review-${dateForFilename}.md`;

    const blob = new Blob([markdown], { type: 'text/markdown;charset=utf-8' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    a.style.display = 'none';

    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  }

  // ==================== 生成 Markdown ====================
  function generateMarkdown() {
    const pageTitle = document.title || t('untitledPage');
    const today = new Date();
    const dateStr = today.toLocaleDateString('zh-CN');
    const year = today.getFullYear();
    const month = String(today.getMonth() + 1).padStart(2, '0');
    const day = String(today.getDate()).padStart(2, '0');
    const dateForFilename = `${year}-${month}-${day}`;

    const pageName = window.location.pathname.split('/').pop().replace('.html', '') || 'prototype';

    const p0 = annotations.filter(a => a.priority === 'P0');
    const p1 = annotations.filter(a => a.priority === 'P1');
    const p2 = annotations.filter(a => a.priority === 'P2');

    // 获取路径：file:// 协议取绝对路径，http(s) 协议取相对路径
    let fullPath = decodeURIComponent(window.location.pathname);
    // file:// 协议下 pathname 就是完整绝对路径
    // http(s) 协议下 pathname 是服务器相对路径（无法获取绝对路径）
    let savePath = '';

    // 提取 HTML 所在目录，拼接 review 文件名
    const dirMatch = fullPath.match(/^(.+)[\/\\][^\/\\]+\.html$/i);
    if (dirMatch) {
      savePath = `${dirMatch[1]}/${pageName}-prototype-review-${dateForFilename}.md`;
    } else {
      savePath = `${pageName}-prototype-review-${dateForFilename}.md`;
    }

    let markdown = `<!--\n`;
    markdown += `${t('savePath')}：${savePath}\n`;
    markdown += `${t('usage')}：${t('usageInstruction')}\n`;
    markdown += `-->\n\n`;

    markdown += `# ${t('reviewRecordTitle')}\n\n`;
    markdown += `> ${t('autoGenerated')}\n\n`;
    markdown += `## ${t('basicInfo')}\n\n`;
    markdown += `- ${t('pageTitle')}：${pageTitle}\n`;
    markdown += `- ${t('reviewDate')}：${dateStr}\n`;
    markdown += `- ${t('totalComments')}：${annotations.length} ${t('commentsCount')}（P0: ${p0.length} / P1: ${p1.length} / P2: ${p2.length}）\n\n`;

    markdown += `## ${t('reviewOpinions')}\n\n`;

    if (p0.length > 0) {
      markdown += `### ${t('p0Must')}\n\n`;
      p0.forEach((a, index) => {
        markdown += `#### ${index + 1}. [${a.type}] ${a.content.split('\n')[0]}\n\n`;
        markdown += `${a.content}\n\n`;
      });
    }

    if (p1.length > 0) {
      markdown += `### ${t('p1Should')}\n\n`;
      p1.forEach((a, index) => {
        markdown += `#### ${index + 1}. [${a.type}] ${a.content.split('\n')[0]}\n\n`;
        markdown += `${a.content}\n\n`;
      });
    }

    if (p2.length > 0) {
      markdown += `### ${t('p2Optional')}\n\n`;
      p2.forEach((a, index) => {
        markdown += `#### ${index + 1}. [${a.type}] ${a.content.split('\n')[0]}\n\n`;
        markdown += `${a.content}\n\n`;
      });
    }

    if (annotations.length === 0) {
      markdown += `${t('noOpinions')}\n`;
    }

    return markdown;
  }

  // ==================== 工具函数 ====================
  function escapeHtml(text) {
    const div = document.createElement('div');
    div.textContent = text;
    return div.innerHTML;
  }

  function copyToClipboard(text) {
    const textarea = document.createElement('textarea');
    textarea.value = text;
    textarea.style.position = 'fixed';
    textarea.style.opacity = '0';
    document.body.appendChild(textarea);
    textarea.select();
    document.execCommand('copy');
    document.body.removeChild(textarea);
  }

