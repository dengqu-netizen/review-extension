  /**
   * 检测页面是否为暗色模式
   * 综合检测：系统偏好 + 页面背景色 + 页面采样
   */
  function detectPageDarkMode() {
    // 1. 检查系统偏好
    const systemPrefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;

    // 2. 检查页面背景色
    const bodyBg = window.getComputedStyle(document.body).backgroundColor;
    const htmlBg = window.getComputedStyle(document.documentElement).backgroundColor;

    const bodyBrightness = calculateBrightness(bodyBg);
    const htmlBrightness = calculateBrightness(htmlBg);

    // 3. 采样页面主要区域的颜色
    const sampledBrightness = samplePageColors();

    // 综合判断：优先使用有效的背景色，如果都是透明则使用采样结果
    let avgBrightness = 255;
    let validSamples = 0;

    // 只有当背景色不是透明时才计入
    if (bodyBrightness < 255) {
      avgBrightness = bodyBrightness;
      validSamples++;
    }
    if (htmlBrightness < 255) {
      avgBrightness = validSamples > 0 ? (avgBrightness + htmlBrightness) / 2 : htmlBrightness;
      validSamples++;
    }

    // 如果 body 和 html 都是透明，采样结果权重更高
    if (validSamples === 0 && sampledBrightness < 255) {
      // 完全依赖采样结果
      console.log('[Review Extension] Using sampled brightness:', sampledBrightness);
      return sampledBrightness < 128;
    } else if (sampledBrightness < 255) {
      // 有背景色时，采样结果作为参考
      avgBrightness = validSamples > 0 ? (avgBrightness + sampledBrightness) / 2 : sampledBrightness;
      validSamples++;
    }

    // 如果有有效采样，使用采样结果；否则使用系统偏好
    const result = validSamples > 0 ? avgBrightness < 128 : systemPrefersDark;

    console.log('[Review Extension] Detection details:', {
      bodyBrightness,
      htmlBrightness,
      sampledBrightness,
      avgBrightness,
      validSamples,
      systemPrefersDark,
      result
    });

    return result;
  }

  /**
   * 采样页面主要区域的颜色
   */
  function samplePageColors() {
    // 增加更多采样点，覆盖页面不同区域
    const samplePoints = [
      { x: window.innerWidth * 0.5, y: window.innerHeight * 0.2 },
      { x: window.innerWidth * 0.5, y: window.innerHeight * 0.5 },
      { x: window.innerWidth * 0.5, y: window.innerHeight * 0.8 },
      { x: window.innerWidth * 0.2, y: window.innerHeight * 0.5 },
      { x: window.innerWidth * 0.8, y: window.innerHeight * 0.5 },
      { x: window.innerWidth * 0.3, y: window.innerHeight * 0.3 },
      { x: window.innerWidth * 0.7, y: window.innerHeight * 0.7 },
    ];

    let totalBrightness = 0;
    let validSamples = 0;

    samplePoints.forEach(point => {
      const element = document.elementFromPoint(point.x, point.y);
      if (element) {
        // 尝试获取元素的背景色，如果透明则向上查找父元素
        let currentElement = element;
        let attempts = 0;
        const maxAttempts = 10; // 最多向上查找10层

        while (currentElement && attempts < maxAttempts) {
          const bgColor = window.getComputedStyle(currentElement).backgroundColor;
          const brightness = calculateBrightness(bgColor);

          if (brightness < 255) {
            // 找到了有效的背景色
            totalBrightness += brightness;
            validSamples++;
            console.log(`[Review Extension] Sampled element at (${Math.round(point.x)}, ${Math.round(point.y)}):`, {
              element: currentElement.tagName,
              bgColor,
              brightness
            });
            break;
          }

          // 背景色是透明的，继续向上查找
          currentElement = currentElement.parentElement;
          attempts++;
        }
      }
    });

    const result = validSamples > 0 ? totalBrightness / validSamples : 255;
    console.log(`[Review Extension] Sampling complete: ${validSamples} valid samples, average brightness: ${result}`);
    return result;
  }

  /**
   * 计算颜色亮度（0-255）
   * 使用感知亮度公式
   */
  function calculateBrightness(color) {
    if (!color || color === 'transparent' || color === 'rgba(0, 0, 0, 0)') {
      return 255; // 默认为亮色
    }

    const rgb = color.match(/\d+/g);
    if (!rgb || rgb.length < 3) return 255;

    const [r, g, b] = rgb.map(Number);
    // 使用感知亮度公式
    return (r * 299 + g * 587 + b * 114) / 1000;
  }

  // ==================== 面板周边颜色检测与主题切换 ====================
  /**
   * 检测面板周边的颜色，判断应该使用亮色还是暗色主题
   * 使用 CSS 检测算法（不完美，但能覆盖大多数情况）
   * @returns {boolean} true = 使用亮色主题（周边是暗色），false = 使用暗色主题（周边是亮色）
   */
  function detectPanelSurroundingColor() {
    if (!reviewPanel) return false;

    const rect = reviewPanel.getBoundingClientRect();

    // 采样距离：平衡避开小元素和不超出目标区域
    const sampleDistance = 50;
    const samplePoints = [
      // 面板底部（3个点）
      { x: rect.left + rect.width * 0.25, y: rect.bottom + sampleDistance },
      { x: rect.left + rect.width * 0.5, y: rect.bottom + sampleDistance },
      { x: rect.left + rect.width * 0.75, y: rect.bottom + sampleDistance },

      // 面板顶部（3个点）
      { x: rect.left + rect.width * 0.25, y: rect.top - sampleDistance },
      { x: rect.left + rect.width * 0.5, y: rect.top - sampleDistance },
      { x: rect.left + rect.width * 0.75, y: rect.top - sampleDistance },

      // 面板左侧（3个点）
      { x: rect.left - sampleDistance, y: rect.top + rect.height * 0.25 },
      { x: rect.left - sampleDistance, y: rect.top + rect.height * 0.5 },
      { x: rect.left - sampleDistance, y: rect.top + rect.height * 0.75 },

      // 面板右侧（3个点）
      { x: rect.right + sampleDistance, y: rect.top + rect.height * 0.25 },
      { x: rect.right + sampleDistance, y: rect.top + rect.height * 0.5 },
      { x: rect.right + sampleDistance, y: rect.top + rect.height * 0.75 },
    ];

    let totalBrightness = 0;
    let validSamples = 0;
    const brightnessValues = [];

    samplePoints.forEach(point => {
      // 确保采样点在视口内
      if (point.x < 0 || point.x > window.innerWidth ||
          point.y < 0 || point.y > window.innerHeight) {
        return;
      }

      const element = document.elementFromPoint(point.x, point.y);
      if (!element || element === reviewPanel || reviewPanel.contains(element)) {
        return;
      }

      // 向上查找父元素，寻找有效的背景色
      let currentElement = element;
      let attempts = 0;
      let brightness = 255;
      let foundValidColor = false;

      while (currentElement && attempts < 6) {
        const computedStyle = window.getComputedStyle(currentElement);
        const bg = computedStyle.backgroundColor;
        const bgImage = computedStyle.backgroundImage;
        
        // 获取元素尺寸
        const elementRect = currentElement.getBoundingClientRect();
        const elementArea = elementRect.width * elementRect.height;

        brightness = calculateBrightness(bg);

        // 判断是否是有效的背景色
        if (brightness < 255) {
          if (bgImage && bgImage !== 'none') {
            // 有背景图片，假设是中等亮度（150）
            // 这样既不会太亮也不会太暗，让检测更保守
            brightness = 150;
            foundValidColor = true;
            break;
          } else if (elementArea > 1500) {
            // 元素面积足够大
            foundValidColor = true;
            break;
          }
        }

        currentElement = currentElement.parentElement;
        attempts++;
      }

      if (foundValidColor && brightness < 255) {
        brightnessValues.push(brightness);
        totalBrightness += brightness;
        validSamples++;
      }
    });

    // 如果没有有效采样，默认使用暗色主题（适合大多数亮色背景）
    if (validSamples === 0) {
      console.log('[Review Extension] No valid samples, using default dark theme');
      return false;
    }

    // 计算平均亮度
    const avgBrightness = totalBrightness / validSamples;

    // 计算中位数（更稳定，不容易被极端值影响）
    brightnessValues.sort((a, b) => a - b);
    const medianBrightness = brightnessValues[Math.floor(brightnessValues.length / 2)];

    // 使用中位数和平均值的加权平均（中位数权重更高）
    const finalBrightness = medianBrightness * 0.7 + avgBrightness * 0.3;
    const isDark = finalBrightness < 128;

    console.log('[Review Extension] Panel surrounding detection (CSS):', {
      avgBrightness: avgBrightness.toFixed(1),
      medianBrightness: medianBrightness.toFixed(1),
      finalBrightness: finalBrightness.toFixed(1),
      validSamples,
      isDark,
      shouldUseLightTheme: isDark,
      allValues: brightnessValues.map(v => v.toFixed(0)).join(', ')
    });

    return isDark;
  }



