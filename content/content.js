  // ==================== 生成 Markdown ====================
  function generateMarkdown() {
    const pageTitle = document.title || t('untitledPage');
    const today = new Date();
    const dateStr = today.toLocaleDateString('zh-CN');
    const year = today.getFullYear();
    const month = String(today.getMonth() + 1).padStart(2, '0');
    const day = String(today.getDate()).padStart(2, '0');
    const dateForFilename = `${year}-${month}-${day}`;

    const pageName = window.location.pathname.split('/').pop().replace('.html', '') || 'prototype';

    const p0 = annotations.filter(a => a.priority === 'P0');
    const p1 = annotations.filter(a => a.priority === 'P1');
    const p2 = annotations.filter(a => a.priority === 'P2');

    // 获取路径：file:// 协议取绝对路径，http(s) 协议取相对路径
    let fullPath = decodeURIComponent(window.location.pathname);
    // file:// 协议下 pathname 就是完整绝对路径
    // http(s) 协议下 pathname 是服务器相对路径（无法获取绝对路径）
    let savePath = '';

    // 提取 HTML 所在目录，拼接 review 文件名
    const dirMatch = fullPath.match(/^(.+)[\/\\][^\/\\]+\.html$/i);
    if (dirMatch) {
      savePath = `${dirMatch[1]}/${pageName}-prototype-review-${dateForFilename}.md`;
    } else {
      savePath = `${pageName}-prototype-review-${dateForFilename}.md`;
    }

    let markdown = `<!--\n`;
    markdown += `${t('savePath')}：${savePath}\n`;
    markdown += `${t('usage')}：${t('usageInstruction')}\n`;
    markdown += `-->\n\n`;

    markdown += `# ${t('reviewRecordTitle')}\n\n`;
    markdown += `> ${t('autoGenerated')}\n\n`;
    markdown += `## ${t('basicInfo')}\n\n`;
    markdown += `- ${t('pageTitle')}：${pageTitle}\n`;
    markdown += `- ${t('reviewDate')}：${dateStr}\n`;
    markdown += `- ${t('totalComments')}：${annotations.length} ${t('commentsCount')}（P0: ${p0.length} / P1: ${p1.length} / P2: ${p2.length}）\n\n`;

    markdown += `## ${t('reviewOpinions')}\n\n`;

    // 生成单条评审记录的 markdown
    function formatAnnotation(a, index) {
      // 获取标注元素的 HTML 开标签
      const elementTag = getElementOpenTag(a);
      markdown += `#### ${index + 1}. [${a.type}]`;
      if (elementTag) {
        markdown += ` \`${elementTag}\``;
      }
      markdown += `\n\n`;
      // 内容：输出全部文本（标题行 + 剩余行）
      markdown += `${a.content.trim()}\n\n`;
    }

    if (p0.length > 0) {
      markdown += `### ${t('p0Must')}\n\n`;
      p0.forEach((a, index) => formatAnnotation(a, index));
    }

    if (p1.length > 0) {
      markdown += `### ${t('p1Should')}\n\n`;
      p1.forEach((a, index) => formatAnnotation(a, index));
    }

    if (p2.length > 0) {
      markdown += `### ${t('p2Optional')}\n\n`;
      p2.forEach((a, index) => formatAnnotation(a, index));
    }

    if (annotations.length === 0) {
      markdown += `${t('noOpinions')}\n`;
    }

    return markdown;
  }

  // ==================== 获取元素开标签 ====================
  // 根据 annotation 的 selector 找到目标元素，提取其开标签（含关键属性）
  function getElementOpenTag(annotation) {
    if (!annotation.selector) return '';
    try {
      const element = findElementSafe(annotation.selector);
      if (!element) return '';

      const tag = element.tagName.toLowerCase();
      // 收集关键属性：class, id, name, type, role, data-* 等
      const attrNames = ['class', 'id', 'name', 'type', 'role', 'placeholder', 'href', 'src'];
      let attrs = '';

      for (const name of attrNames) {
        const val = element.getAttribute(name);
        if (val) {
          // 过滤掉插件自身的 dq-review- 类名
          if (name === 'class') {
            const filtered = val.split(/\s+/)
              .filter(c => c && !c.startsWith('dq-review-'))
              .join(' ');
            if (filtered) {
              attrs += ` class="${filtered}"`;
            }
          } else {
            attrs += ` ${name}="${val}"`;
          }
        }
      }

      // 补充 data-* 属性
      if (element.dataset) {
        for (const [key, val] of Object.entries(element.dataset)) {
          if (val && !key.startsWith('dqReview')) {
            attrs += ` data-${key.replace(/([A-Z])/g, '-$1').toLowerCase()}="${val}"`;
          }
        }
      }

      return `<${tag}${attrs}>`;
    } catch (e) {
      console.warn('[Review Extension V2] getElementOpenTag failed:', e.message);
      return '';
    }
  }

