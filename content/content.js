  // ==================== 增强的元素选择器（架构改进2） ====================

  function getElementSelector(element) {
    // 优先用 ID（最稳定）
    if (element.id) {
      return '#' + cssEscape(element.id);
    }

    const path = [];
    let current = element;

    while (current && current.nodeType === Node.ELEMENT_NODE) {
      let selector = current.nodeName.toLowerCase();

      // 用 CSS.escape 安全转义 class 名
      if (current.className && typeof current.className === 'string') {
        const classes = current.className.split(' ')
          .filter(c => c.trim() && !c.startsWith('dq-review-'))
          .map(c => cssEscape(c.trim()));
        if (classes.length > 0) {
          selector += '.' + classes.join('.');
        }
      }

      // nth-child 保证唯一性
      if (current.parentNode) {
        const siblings = Array.from(current.parentNode.children);
        const index = siblings.indexOf(current) + 1;
        selector += `:nth-child(${index})`;
      }

      path.unshift(selector);
      current = current.parentNode;

      if (current && (current.nodeName === 'BODY' || current.nodeName === 'HTML')) {
        break;
      }

      if (path.length > 8) break;
    }

    return path.join(' > ');
  }

  // CSS.escape polyfill（兼容不支持的环境）
  function cssEscape(value) {
    if (typeof CSS !== 'undefined' && CSS.escape) {
      return CSS.escape(value);
    }
    // 手动转义特殊字符
    return value.replace(/([^\w-])/g, '\\$1');
  }

  // 安全查找元素（容错）
  function findElementSafe(selector) {
    if (!selector) return null;
    try {
      return document.querySelector(selector);
    } catch (e) {
      console.warn('[Review Extension V2]', t('selectorFailed'), selector, e.message);
      // 备用：去掉类名部分，只用标签 + nth-child
      try {
        const simplified = selector.replace(/\.[^:>\s]+/g, '');
        if (simplified !== selector) {
          return document.querySelector(simplified);
        }
      } catch (e2) {
        console.warn('[Review Extension V2]', t('backupFailed'), e2.message);
      }
      return null;
    }
  }

